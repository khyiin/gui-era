    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        config con = new config();
        config.Session sess = config.Session.getInstance();

        // 1. Collect new data from fields
        String fn = firstname.getText();
        String ln = lastname.getText();
        String em = email.getText();
        String un = username.getText();
        String ut = usertype.getText().toString();

        // 2. Update the Database
        String sql = "UPDATE users SET first_name = ?, last_name = ?, email = ?, username = ?, user_type = ? WHERE id = ?";
        con.updateRecord(sql, fn, ln, em, un, ut, sess.getUid());

        // 3. Update the Session (so the app "remembers" the new names immediately)
        sess.setFirst_name(fn);
        sess.setLast_name(ln);
        sess.setEmail(em);
        sess.setUsername(un);
        sess.setUser_type(ut);

        JOptionPane.showMessageDialog(null, "Changes Saved Successfully!");

        int currentID = config.Session.getInstance().getUid();

        // Pass that ID into the constructor
        new landlordprofile(currentID).setVisible(true);
        this.dispose();
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

